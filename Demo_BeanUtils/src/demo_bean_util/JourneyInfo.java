package demo_bean_util;

public class JourneyInfo {

	String journeyInfoId;
	String journeyInfoName;
	
	public String getJourneyInfoId() {
		return journeyInfoId;
	}
	public void setJourneyInfoId(String journeyInfoId) {
		this.journeyInfoId = journeyInfoId;
	}
	public String getJourneyInfoName() {
		return journeyInfoName;
	}
	public void setJourneyInfoName(String journeyInfoName) {
		this.journeyInfoName = journeyInfoName;
	}
	
	
	
}
