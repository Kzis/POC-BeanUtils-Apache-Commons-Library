package demo_bean_util;

public class BeanUnicorn {
	
	String colour;
	String leg;
	String name;
	
	public String getColour() {
		return colour;
	}
	public void setColour(String i) {
		this.colour = i;
	}
	public String getLeg() {
		return leg;
	}
	public void setLeg(String g) {
		this.leg = g;
	}
	public String getName() {
		return name;
	}
	public void setName(String h) {
		this.name = h;
	}
	
	

}
